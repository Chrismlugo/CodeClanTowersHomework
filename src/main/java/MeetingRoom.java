

public class MeetingRoom extends Room{
    int capacity;
    private String name;
    private double price;

    public MeetingRoom(int capacity, String name, double price){
        super();
        this.name = name;
        this.price = price;
        this.capacity = capacity;

    }

    public void addGuest(Guest guest){
        if(countGuests() < capacity){
            guests.add(guest);
        }
    }
}
